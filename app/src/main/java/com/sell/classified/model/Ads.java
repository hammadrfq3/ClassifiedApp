package com.sell.classified.model;

public class Ads {

    private int LatestAdId;
    private String LatestAdName;
    private String LatestAdImage;
    private String LatestAdPrice;
    private String LatestAdAddress;

    public int getLatestAdId() {
        return LatestAdId;
    }

    public void setLatestAdId(int latestAdId) {
        LatestAdId = latestAdId;
    }

    public String getLatestAdName() {
        return LatestAdName;
    }

    public void setLatestAdName(String latestAdName) {
        LatestAdName = latestAdName;
    }

    public String getLatestAdImage() {
        return LatestAdImage;
    }

    public void setLatestAdImage(String latestAdImage) {
        LatestAdImage = latestAdImage;
    }

    public String getLatestAdPrice() {
        return LatestAdPrice;
    }

    public void setLatestAdPrice(String latestAdPrice) {
        LatestAdPrice = latestAdPrice;
    }

    public String getLatestAdAddress() {
        return LatestAdAddress;
    }

    public void setLatestAdAddress(String latestAdAddress) {
        LatestAdAddress = latestAdAddress;
    }
}
