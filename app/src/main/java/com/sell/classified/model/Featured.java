package com.sell.classified.model;

public class Featured {

    private int featuredId;
    private String featuredItemName;
    private String featuredItemImage;
    private String featuredItemPrice;
    private String featuredItemAddress;

    public int getFeaturedId() {
        return featuredId;
    }

    public void setFeaturedId(int featuredId) {
        this.featuredId = featuredId;
    }

    public String getFeaturedItemName() {
        return featuredItemName;
    }

    public void setFeaturedItemName(String featuredItemName) {
        this.featuredItemName = featuredItemName;
    }

    public String getFeaturedItemImage() {
        return featuredItemImage;
    }

    public void setFeaturedItemImage(String featuredItemImage) {
        this.featuredItemImage = featuredItemImage;
    }

    public String getFeaturedItemPrice() {
        return featuredItemPrice;
    }

    public void setFeaturedItemPrice(String featuredItemPrice) {
        this.featuredItemPrice = featuredItemPrice;
    }

    public String getFeaturedItemAddress() {
        return featuredItemAddress;
    }

    public void setFeaturedItemAddress(String featuredItemAddress) {
        this.featuredItemAddress = featuredItemAddress;
    }
}
